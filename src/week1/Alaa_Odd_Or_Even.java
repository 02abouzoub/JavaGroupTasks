package week1;
import java.util.Scanner;
public class Alaa_Odd_Or_Even {
    public static void main(String[] args) {

        Scanner sc = new Scanner(System.in);
        System.out.println("Enter a number:");
        int num = sc.nextInt();
        if (num % 2 ==0){
            System.out.println("Even");
        }
        else
        {
            System.out.println("odd");
        }





    }
}
